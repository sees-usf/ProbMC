package dipro.stoch.prism;

import parser.Values;

public class ExplicitPrismState extends PrismState {

	protected int id; 
	
	public ExplicitPrismState(Values v, int id) {
		super(v);
		this.id = id;
	}

	public int getId() {
		return id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = prime + id;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (getClass() != obj.getClass())
			return false;
		ExplicitPrismState other = (ExplicitPrismState) obj;
		if (id != other.id) {
			assert !super.equals(obj);
			return false;
		}
		assert super.equals(obj);
		return true;
	}
	
	
}
